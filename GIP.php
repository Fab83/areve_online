            <h3 class="greenUp">Groupement d'Intérêt Public AREVE</h3>
            <h5 class="greenLo pb-2">(Agence de Rénovation Energétique Var Est)</h5>
            <p>L'AREVE est une Plateforme de la rénovation énergétique, issue d'un Appel à Manifestation d'Intérêt lancé par <a href="http://www.ademe.fr/">l'ADEME</a> et <a href="https://www.maregionsud.fr/">le Conseil Régional Sud Provence-Alpes-Côte d’Azur</a>.<br>Portée par des collectivités territoriales ayant montré une volonté forte d'accompagner cette politique sur leur territoire, elles ont choisi de se regrouper sous la forme d'un Groupement d'Intérêt Public.<br><br>Définis au titre de la <a href="https://www.legifrance.gouv.fr/affichTexte.do?cidTexte=JORFTEXT000024021430&categorieLien=id">loi n°2011-525 du 17 mai 2011 de simplification et d’amélioration de la qualité du droit,</a> les Groupements d’Intérêt Public (GIP) sont des personnes morales de droit public qui ont pour objet de permettre à leurs membres d’exercer ensemble des activités d’intérêt général à but non lucratif, en mettant en commun les moyens nécessaires à leur exercice.<br><br>
            Depuis le 1er janvier 2021, l'AREVE est devenue un Espace Conseil FAIRE inscrit dans le SARE (Service d'accompagnement à la Rénovation Energétique). Ses missions restent inchangées auprès des particuliers et des copropriétés, mais elle complète ses actions de conseil en accompagnant les entreprises du "petit tertiaire" (locaux professionnels dont la surface est < 1000 m²).<br>Au-delà de l'évolution de ses missions, son entrée dans le SARE a modifié son mode de financement et a fait entrer le Conseil Départemanetal du Var comme nouveau partenaire en sa qualité de "porteur associé".<br>Pour plus d'informations sur le SARE et sur les différents acteurs, rendez-vous sur <a href="https://www.ademe.fr/sare-service-daccompagnement-a-renovation-energetique">le site de l'ADEME.</a></p>
            <h5 class="greenUp">Notre action</h5>
            <p>Depuis sa création, l'AREVE a accompagné plus de 4.000 ménages dans leur projet de rénovation.<br>Depuis le 1er janvier 2021, ce sont plus de 500 ménages qui nous ont contacté pour obtenir des informations techniques, ou sur les aides financières, ou encore sur la lecture de devis ou un problème administratif.<br><a href="assets/fiche_AREVE_juin_2021.pdf">Présentation du bilan des contacts pour l'année 2021 (au 15 juin).</a></p>
