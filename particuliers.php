					<b>La première étape : l’isolation</b>
					<p>Rien ne sert de changer votre système de chauffage si la chaleur s’échappe de votre logement. Des travaux d’isolation vous apporteront immédiatement plus de confort et seront très rentables sur le long terme.<br>
					Vérifiez l’isolation de votre toit car c’est par le toit que 25 à 30 % de la chaleur s’échappe. Isolez vos combles, qu’ils soient aménagés ou non.<br>Isolez aussi les murs surtout ceux exposés au nord et aux vents. Plus les murs sont isolés, moins ils se refroidissent vite et une moindre différence entre la température des murs et la pièce vous apportera un bien meilleur confort.
					<br>Si votre maison est bâtie sur un vide-sanitaire ou un sous-sol, isolez le plancher bas.
					<br>Remplacez vos fenêtres si elles sont à simple-vitrage et si elles laissent s’infiltrer de l’air. C’est souvent par cette action que l’on a envie de commencer les travaux alors que seulement 10 à 15 % de la chaleur s’échappe par les fenêtres. 
					<br>Certains travaux d’isolation thermique permettent également d’améliorer l’isolation phonique du logement… mais pas toujours ! <br>Si vous avez des problèmes de bruit chez vous, pensez à bien demander aux professionnels de l’isolation si vous pouvez résoudre les deux problèmes en une seule fois.<br><i>Pour en savoir plus, contactez l'AREVE ou consultez <a href="https://www.ademe.fr/sites/default/files/assets/documents/guide-pratique-isoler-sa-maison.pdf">les guides pratiques ADEME.</a> </i>
					<br>
					<br>
					<b>N’isolez pas sans revoir aussi la ventilation</b>
					<br>L’air d’un logement doit absolument être renouvelé afin d’évacuer tous les polluants dans l’air intérieur. Bien isolé, un logement ne laisse plus place aux infiltrations d’air. Il faut donc prévoir une ventilation mécanique contrôlée pour gérer les entrées et les sorties d’air. Ce poste de travaux est indispensable pour ne pas dégrader le logement et nuire à la santé des occupants.
					<br>
					Pour en savoir plus sur les différents systèmes de ventilation, consultez <a href="https://www.ademe.fr/sites/default/files/assets/documents/guide-pratique-ventilation-logement-confortable-sain.pdf">le guide ADEME sur « La ventilation ».</a> 
